// diff-size-count.mjs — Parse git diff --numstat output and sum changed lines,
// excluding paths that match any glob in the ignoreList.
//
// Exports parseDiffNumstat(rawNumstat, ignoreList) for unit testing.
// When run as a CLI, reads numstat from stdin and ignoreList from brain.config.json.
//
// The glob matcher is reused from brain/scripts/lib/installer.mjs (matchesAny) —
// no additional dependencies are introduced (brain is zero-deps).
//
// DOCTRINE (issue #253/B1 fresh-review ruling): ignoreList classifies review
// surface, not file size. A machine-generated artifact whose GENERATOR is
// reviewed (e.g. a `**/*.golden.json` snapshot produced and asserted-against
// by a committed, reviewed test) is test data, not review surface — the
// reviewer reads the generator, not every generated byte. This is why
// `**/*.golden.json` is scoped to that exact suffix in brain.config.json's
// governance.ignoreList: broad enough to exempt legitimate generated
// snapshots, narrow enough that it is not a general size-budget escape hatch.

import { readFileSync } from 'node:fs';
import { createInterface } from 'node:readline';
import { fileURLToPath } from 'node:url';

import { matchesAny } from '../lib/installer.mjs';

// ── Core function ─────────────────────────────────────────────────────────────

/**
 * Parses `git diff --numstat` output and returns the total changed lines
 * (additions + deletions) for files not matched by any ignoreList glob.
 *
 * Binary files are represented by `-` in place of numeric counts and are
 * treated as 0 changed lines (they have no line-based diff).
 *
 * @param {string}   rawNumstat  Raw stdout from `git diff --numstat`.
 * @param {string[]} ignoreList  Glob patterns using the same syntax as
 *                               brain/core/managed-paths.mjs (supports `*`
 *                               and `**`). Paths matching any glob are excluded
 *                               from the total.
 * @returns {number}  Total additions + deletions for non-excluded files.
 */
export function parseDiffNumstat(rawNumstat, ignoreList) {
  if (!rawNumstat || !rawNumstat.trim()) return 0;

  let total = 0;
  for (const line of rawNumstat.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed) continue;

    // Format: <added>\t<deleted>\t<path>
    const tab1 = trimmed.indexOf('\t');
    const tab2 = trimmed.indexOf('\t', tab1 + 1);
    if (tab1 === -1 || tab2 === -1) continue;

    const addStr = trimmed.slice(0, tab1);
    const delStr = trimmed.slice(tab1 + 1, tab2);
    const path = trimmed.slice(tab2 + 1);

    if (!path) continue;
    if (ignoreList.length > 0 && matchesAny(path, ignoreList)) continue;

    // Binary files use `-` in place of counts — treat as 0.
    const add = addStr === '-' ? 0 : Number(addStr) || 0;
    const del = delStr === '-' ? 0 : Number(delStr) || 0;
    total += add + del;
  }

  return total;
}

// ── CLI entry-point ───────────────────────────────────────────────────────────
// When invoked directly via `node brain/scripts/vcs/diff-size-count.mjs`, reads
// numstat from stdin and ignoreList from brain.config.json in the working
// directory. Prints the total changed line count to stdout and exits 0.
// The caller (e.g., the governance.yml workflow) is responsible for deciding
// whether the count exceeds the budget.

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  // Load ignoreList from brain.config.json in the working directory.
  let ignoreList = [];
  try {
    const config = JSON.parse(readFileSync('brain.config.json', 'utf8'));
    ignoreList = Array.isArray(config?.governance?.ignoreList)
      ? config.governance.ignoreList
      : [];
  } catch {
    // Config absent or governance section missing — proceed with no exclusions.
  }

  // Read numstat from stdin.
  const lines = [];
  for await (const line of createInterface({ input: process.stdin })) {
    lines.push(line);
  }
  const raw = lines.join('\n');

  process.stdout.write(String(parseDiffNumstat(raw, ignoreList)) + '\n');
}
